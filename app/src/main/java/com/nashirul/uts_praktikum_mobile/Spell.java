package com.nashirul.uts_praktikum_mobile;

public class Spell {
        private String name;
        private String desc;

        public Spell(String name, String desc) {
            this.name = name;
            this.desc = desc;
        }

        public String getName() {
            return name;
        }

        public String getDesc() {
            return desc;
        }
    }
